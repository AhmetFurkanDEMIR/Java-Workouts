public class Pixel {

    private int en;
    private int boy;

    public Pixel(int en, int boy){
        this.setEn(en);
        this.setBoy(boy);
    }

    public int getEn() {
        return en;
    }

    public void setEn(int en) {
        this.en = en;
    }

    public int getBoy() {
        return boy;
    }

    public void setBoy(int boy) {
        this.boy = boy;
    }
}
